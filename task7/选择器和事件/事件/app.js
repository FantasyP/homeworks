$(document).ready(function(){
    $('button').click(function(){
        $(this).hide();
    });
    $('button').dbclick(function(){
        $(this).hide();
    });
    $('button').mouseenter(function(){
        $(this).hide();
    });
    $('button').mouseleave(function(){
        $(this).hide();
    });
});